package introduction1;

public class DataTypes3 {

	public static void main(String[] args) {
		int numberFive = 5;
		float numberWithDecimalPoints = 5.99f;
		char letterD = 'D';
		boolean trueBooleanValue = true;
		String stringValue = "Hello";

		System.out.println("numberFive: " + numberFive);
		System.out.println("numberWithDecimalPoints: " + numberWithDecimalPoints);
		System.out.println("letterD: " + letterD);
		System.out.println("trueBooleanValue: " + trueBooleanValue);
		System.out.println("stringValue: " + stringValue);
	}

}
